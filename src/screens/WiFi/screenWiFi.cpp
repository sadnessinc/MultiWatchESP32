void screenWiFi() {
    display.clearDisplay();
    display.setTextColor(SSD1306_WHITE);

    auto mode = wifiNetwork.getCurrentEnumWiFiMode();
    bool apOn = (mode == WiFiNetwork::WiFiMode::AP || mode == WiFiNetwork::WiFiMode::AP_STA);
    bool staOn = (mode == WiFiNetwork::WiFiMode::STA || mode == WiFiNetwork::WiFiMode::AP_STA);
    bool staConnected = wifiNetwork.isConnected();

    display.setTextSize(2);
    display.setCursor(0, 0);
    display.println("WiFi");

    int x = SCREEN_WIDTH - 32;
    int y = 16;

    if (staConnected) {
        display.drawBitmap(x, y, wifi32Image, 32, 32, SSD1306_WHITE);
    } else {
        display.drawBitmap(x, y, wifi32disableImage, 32, 32, SSD1306_WHITE);
    }

    display.setTextSize(2);
    display.setCursor(2, 16);
    display.println("Mode:");


    //display.println(wifiNetwork.toStringWiFiMode(mode));


    display.setCursor(4, display.getCursorY());
    display.setTextSize(1);
    display.print("AP: ");
    display.println(apOn ? "ON" : "OFF");

    display.setCursor(4, display.getCursorY() + 2);
    display.print("STA: ");
    if (!staOn) {
        display.println("OFF");
    } else {
        display.println(staConnected ? "CONNECTED" : "READY");
    }

    int arrowY = display.getCursorY() + 2;
    display.drawBitmap(2, arrowY, arrowLeft, 8, 8, SSD1306_WHITE);
    display.drawBitmap(2 + 8 + 50, arrowY, arrowRight, 8, 8, SSD1306_WHITE);

    display.display();
}

void updateWiFiInput() {
    if (input.pressed(BTN_BACK)) {
        setScreen(MENU);
        return;
    }

    if (input.pressed(BTN_LEFT)) {
        wifiNetwork.prevWiFiMode();
    }

    if (input.pressed(BTN_RIGHT)) {
        wifiNetwork.nextWiFiMode();
    }

    if (input.pressed(BTN_DOWN)) {
        if (wifiNetwork.isConnected()) {
            drawLoading("DISCONNECTING");
            wifiNetwork.setAutoOff();
            wifiNetwork.disconnect();
        } else {
            auto mode = wifiNetwork.getCurrentEnumWiFiMode();

            if (mode == WiFiNetwork::WiFiMode::OFF) {
                wifiNetwork.changeWiFiMode(WiFiNetwork::WiFiMode::STA);
                wifiNetwork.connect();
                return;
            }

            if (mode == WiFiNetwork::WiFiMode::STA || mode == WiFiNetwork::WiFiMode::AP_STA) {
                drawLoading("CONNECTING");
                wifiNetwork.setAutoOff(0);
                wifiNetwork.connect();
            }
        }
    }
}